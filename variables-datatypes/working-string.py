phrase = "Lucky Fauzi"

# New Line with "\n"
print("Lucky\nFauzi")

# Add quote "\"
print("\"Lucky Fauzi\"")

# Concatenation
print(phrase + " Is Handsome")

# With build-in Function
# .lower()
print(phrase.lower())
 
# .upper()
print(phrase.upper())

# .islower()
print(phrase.islower())

# .isupper()
print(phrase.isupper())

# combined function
print(phrase.upper().isupper())

# len() function
print(len(phrase))

# index
print(phrase[0])
print(phrase.index("ky"))

# .replace(str, newstr)
print(phrase.replace("Lucky", "Leon"))