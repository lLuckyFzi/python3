# Decimal
print(4)

# Float
print(4.44444)

# Operators
print(4 + 10)
print(4 - 10)
print(4 * 10)
print(4 / 10)
print((40 - 2) * 2)

print(4 % 3)

# With variables
my_num = -40
print(my_num)

# With function

# str()
print(str(my_num) + " is not my age")

# abs()
print(abs(my_num))

# pow()
print(pow(5, 2))

# max()
print(max(5, 20))

# min()
print(min(5, 20))

# round()
print(round(4.8))

# With build-in lib
from math import *

# floor()
print(floor(4.2))

# ceil()
print(ceil(4.2))

# sqrt()
print(sqrt(4))